// Задача - написать цикл, который будет задавать вопросы пользователю
// Записывать их в переменные
// Выводить ответы на экран

// Создаем массив ответов и массив вопросов
let answers = [],
    questions = [
        'Как вас зовут?',
        'Какая ваша фамилия?',
        'Сколько вам лет?'
    ];
// Создаем цикл, который начинается с нулевого значения итератора
// продолжается, пока значение i меньше количества значений в массиве questions
// В каждой итерации в массив answers записываем ответ и увеличиваем i на 1
for (let i = 0; i < questions.length; i++) {
    answers[i] = prompt(questions[i], '');
}
document.write(answers);


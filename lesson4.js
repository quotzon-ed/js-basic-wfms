// Цикл do while (цикл с постусловием)
// Данный цикл выводит в консоли значение переменной i
// Увеличивая ее каждый раз на 1
// Цикл выполняется до тех пор, пока i принимает значения меньше 10
let i = 0;
do {
    console.log(i);
    i++;
} while(i < 10);

// Цикл while (цикл с предусловием)
// Данный цикл выводит в консоли значение переменной a
// Увеличивая ее каждый раз на 1
// Цикл выполняется до тех пор, пока a принимает значения меньше 10
// В отличие от предыдущего примера, сначала проверяется условие выполнения
// А затем уже запускается цикл (при выполнении заданного условия)
let a = 0;
while (a < 10) {
    console.log(a);
    a++;
}

